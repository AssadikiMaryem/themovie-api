Route::get('/', function () {
    return view('welcome');
});

Route::middleware([
    'auth:sanctum',
    config('jetstream.auth_session'),
    'verified',
])->group(function () {
    Route::get('/dashboard', function () {
        return view('dashboard');
    })->name('dashboard');
})->group(function () {
    Route::get('/movies', [MovieController::class, 'index'])->name('movies');
})->group(function () {
    Route::get('/movies/{id}', [MovieController::class, 'show'])->name('movies.show');
});
